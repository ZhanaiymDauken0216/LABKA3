#Үстел ойыны үшін 1-ден N-ге дейінгі сандары бар карталар пайдаланылады.Бір карта жоғалады. Қалған карталардың сандарын білу арқылы оны табыңыз.
#N саны берілген, содан кейін N − 1 қалған карталардың саны (1-ден N-ге дейінгі әртүрлі сандар). Бағдарлама жоғалған картаның нөмірін көрсетуі керек.
n = int(input())
sum = 0
for i in range(1, n + 1):
    sum += i

# sum == n * (n + 1) // 2
# формуланы цикл арқылы жасаймыз
for i in range(n - 1):
    sum -= int(input())
print(sum)